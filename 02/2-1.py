# 정수형
a = 123
b = -178
c = 0

print(a)
print(b)
print(c)

print("--------------------")
# 실수형
a1=1.2
a2=-3.45
a3 = 4.24E10
a4 = 4.24e-10

print(a1)
print(a2)
print(a3)
print(a4)

print("--------------------")

# 8진수와 16진수
b1 = 0o177 # 8진수
b2 = 0x8ff # 16진수
b3 = 0xABC # 16진수

print(b1)
print(b2)
print(b3)

print("--------------------")

# 사칙 연산
c1 = 3
c2 = 4

print(c1+c2)
print(c1-c2)
print(c1*c2)
print(c1/c2)

print("--------------------")

# x의 y제곱을 나타내는 ** 연산자

x1 = 3
x2 = 4

print(x1**x2)

print("--------------------")

# 나눗셈 후 나머지를 리턴하는 % 연산자
print(7%3)
print(3%7)

print("--------------------")

# 나눗셈 후 몫을 리턴하는 // 연산자

print(7//4)

print("--------------------")

# 복합 연산자

d = 1
d+=1
print(d)

c=1
c-=1
print(c)